// Get number of Suktas in a Mandala
export function getSuktaCount(mandala: number): number {
  return rigvedaStructure[mandala]?.suktas || 0;
}

// Get number of verses in a specific Sukta
export function getVerseCount(mandala: number, sukta: number): number {
  return rigvedaStructure[mandala]?.verses[sukta] || 0;
}

// Get all Sukta numbers for a Mandala
export function getSuktaNumbers(mandala: number): number[] {
  const info = rigvedaStructure[mandala];
  if (!info) return [];
  return Array.from({ length: info.suktas }, (_, i) => i + 1);
}

// Get all verse numbers for a specific Sukta
export function getVerseNumbers(mandala: number, sukta: number): number[] {
  const count = getVerseCount(mandala, sukta);
  if (!count) return [];
  return Array.from({ length: count }, (_, i) => i + 1);
}

// Validate if a location exists
export function isValidLocation(
  mandala: number,
  sukta: number,
  verse: number
): boolean {
  const info = rigvedaStructure[mandala];
  if (!info) return false;
  if (sukta < 1 || sukta > info.suktas) return false;
  const verseCount = info.verses[sukta];
  if (!verseCount) return false;
  return verse >= 1 && verse <= verseCount;
}
